    private void btnSearchActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionActionPerformed
        if(dateDariTanggal.getDate()==null || dateSampaiTanggal.getDate()==null){
            JOptionPane.showMessageDialog(Main.getMainFrame(), "Range tanggal harus diisi !!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String sdate = sdf.format(dateDariTanggal.getDate());
        String edate = sdf.format(dateSampaiTanggal.getDate());
        
        List<PenilaianDTO> dtos = new ArrayList<PenilaianDTO>();
        try {
            if(karyawanSearch != null){
                if(cmbSearchOption.getSelectedItem().equals("ABSENSI")){
                    dtos = Main.getService().hitungRekapAbsensi(karyawanSearch, sdate, edate);
                } else if(cmbSearchOption.getSelectedItem().equals("POINT")) {
                    dtos = Main.getService().hitungRekapPoint(karyawanSearch, sdate, edate);
                } else {
                    dtos = Main.getService().hitungRekapAll(karyawanSearch, sdate, edate);
                }
            } else {
                if(cmbSearchOption.getSelectedItem().equals("ABSENSI")){
                    dtos = Main.getService().hitungRekapAbsensi(sdate, edate);
                } else if(cmbSearchOption.getSelectedItem().equals("POINT")) {
                    dtos = Main.getService().hitungRekapPoint(sdate, edate);
                } else {
                    dtos = Main.getService().hitungRekapAll(sdate, edate);
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(Main.getMainFrame(), ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        tblPenilaian.setModel(new RekapHarianTableModel(dtos));
        TableUtil.initColumn(tblPenilaian);
    }//GEN-LAST:event_btnSearchActionActionPerformed

