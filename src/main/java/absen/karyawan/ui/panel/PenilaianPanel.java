    private void btnSimpanAbsensiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanAbsensiActionPerformed
        if(karyawanAbsensi!=null){
            try {
                Absensi abs = new Absensi();
                abs.setKaryawan(karyawanAbsensi);
                abs.setTanggal(new Date());
                abs.setHadir(checkHadir.isSelected());
                Main.getService().simpan(abs);
                btnBatalAbsensiActionPerformed(evt);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(Main.getMainFrame(), ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(Main.getMainFrame(), "karyawan absensi tidak boleh null", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSimpanAbsensiActionPerformed

